class HomePage extends StatelessWidget {
  Future<List<Map<String, dynamic>>> _fetchGrades() async {
    final response = await http.get(Uri.parse('http://localhost:3000/grades'));
    if (response.statusCode == 200) {
      List<dynamic> grades = jsonDecode(response.body);
      return grades.cast<Map<String, dynamic>>();
    } else {
      throw Exception('Failed to load grades');
    }
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(title: Text('Home')),
      body: FutureBuilder<List<Map<String, dynamic>>>(
        future: _fetchGrades(),
        builder: (context, snapshot) {
          if (snapshot.connectionState == ConnectionState.waiting) {
            return Center(child: CircularProgressIndicator());
          } else if (snapshot.hasError) {
            return Center(child: Text('Error: ${snapshot.error}'));
          } else {
            List<Map<String, dynamic>>? grades = snapshot.data;
            return ListView.builder(
              itemCount: grades?.length ?? 0,
              itemBuilder: (context, index) {
                return ListTile(
                  title: Text(grades![index]['nama']),
                  subtitle: Text('Nilai: ${grades[index]['nilai']}'),
                );
              },
            );
          }
        },
      ),
    );
  }
}
